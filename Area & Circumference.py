# Program to calculate area and circumference of a circle
pi=3.14
r=float(input("Enter the radius:"))   # Takes the radius as input
a=pi*r*r                              # Area = pi*(radius)^2
c=2*pi*r                              # Circumference = 2*pi*r
print("Area is:", a)                  # Prints area as output
print("Circumference is:", c)         # Prints circumference as output
