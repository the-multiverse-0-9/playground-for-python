# Input two numbers and display the larger / smaller number
a = int(input("Enter any number:")) # Takes first number as input
b = int(input("Enter another number:")) # Takes second number as input
if a>b :
    print("The greater number is:", a) # Displays output
    print("The smaller number is:", b) # Displays output
elif a==b :
    print("Both the numbers are equal") # Displays output
elif a<b :
    print("The greater number is:", b) # Displays output
    print("The smaller number is:", a) # Displays output
else:
    print("Probably you have done something wrong.") # Displays output
    print("Try again.") # Displays output
 
 
