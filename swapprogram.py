# Program to swap values of two variables
a,b = 7,5                               # a = 7 & b = 5
print("The initial value of a is:", a)  # Prints the initial value of variable 'a'
print("The initial value of b is:", b)  # Prints the initial value of variable 'b'  
a,b = b,a                               # Swaps the values of 'a' & 'b'
print("The final value of a is:", a)    # Prints the final value of variable 'a'
print("The final value of b is:", b)    # Prints the final value of variable 'b'
